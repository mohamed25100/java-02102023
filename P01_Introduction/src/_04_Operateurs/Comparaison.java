package _04_Operateurs;

public class Comparaison {

	public static void main(String[] args) {

		int a = 1, b = 2;
		
		System.out.println("a == b : " + (a == b));
		System.out.println("a != b : " + (a != b));
		System.out.println("a > b : " + (a > b));
		System.out.println("a < b : " + (a < b));
		System.out.println("a <= b : " + (a <= b));
		System.out.println("a >= b : " + (a >= b));
	}
}
